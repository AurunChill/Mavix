#!/usr/bin/env python3
"""UML Use Case — администратор и оператор системы Mavix (ортогональные связи)."""
from __future__ import annotations

import pathlib

from diagramlib import Canvas

c = Canvas(1580, 1010)
RX, RY = 140, 42

# Граница системы
c.rect(370, 70, 820, 860, sw=1.6)
c.text(780, 102, 'Mavix', fs=18, bold=True)

# Акторы (снаружи границы)
c.actor(155, 380, 'Администратор')
c.actor(1425, 380, 'Оператор')
AY = 452  # уровень «тела» актора для горизонтального отвода

# Общий сценарий
SH = (780, 150)
c.ellipse(*SH, RX, RY, 'Вход в систему')

# Сценарии администратора (левая колонка) и оператора (правая)
admin_uc = [
    (575, 300, ['Создать', 'оператора']),
    (575, 410, ['Скачать дистрибутив', 'борта']),
    (575, 520, ['Создать заявку', 'на доставку']),
    (575, 630, ['Журнал', 'доставок']),
    (575, 745, ['Управление', 'дронами']),
]
op_uc = [
    (985, 300, ['Принять', 'заявку']),
    (985, 455, ['Управление', 'дроном']),
    (985, 615, ['Просмотр видео', 'и телеметрии']),
    (985, 775, ['Сбросить', 'груз']),
]
for x, y, t in admin_uc + op_uc:
    c.ellipse(x, y, RX, RY, t)

# --- ассоциации актор—сценарии: вертикальный «ствол» + горизонтальные ветви ---
AT, OT = 410, 1150  # x вертикальных стволов (внутри границы)
# администратор
c.line(190, AY, AT, AY)                 # актор → ствол
c.line(AT, SH[1], AT, 745)              # ствол (от уровня «Вход» до нижнего UC)
c.line(AT, SH[1], SH[0] - RX, SH[1])    # ветвь к «Вход в систему»
for x, y, _ in admin_uc:
    c.line(AT, y, x - RX, y)            # ветви к UC
# оператор
c.line(1390, AY, OT, AY)
c.line(OT, SH[1], OT, 775)
c.line(OT, SH[1], SH[0] + RX, SH[1])
for x, y, _ in op_uc:
    c.line(OT, y, x + RX, y)

# --- include / extend (пунктир, открытый наконечник) --------------------------
# «Управление дроном» ──include──▷ «Просмотр видео и телеметрии» (всегда включает)
c.poly([(985, 455 + RY), (985, 615 - RY)], marker='open', dash=True)
c.text(995, 540, '«include»', fs=12, anchor='start', italic=True)
# «Сбросить груз» ──extend──▷ «Управление дроном» (необязательное расширение)
c.poly([(985 - RX, 775), (815, 775), (815, 455), (985 - RX, 455)], marker='open', dash=True)
c.text(805, 620, '«extend»', fs=12, anchor='end', italic=True)

out = pathlib.Path(__file__).parent / 'use_case.svg'
out.write_text(c.svg('Рисунок 5 – Диаграмма вариантов использования (Use Case) системы Mavix'), encoding='utf-8')
print(out)
