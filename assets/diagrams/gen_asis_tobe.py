#!/usr/bin/env python3
"""AS-IS / TO-BE — процесс срочной доставки медицинских грузов (вымышленная
сеть лабораторий и клиник): как сейчас и как с аппаратно-программным комплексом."""
from __future__ import annotations

import pathlib

from diagramlib import Canvas

c = Canvas(1720, 760)


def row(y, title, boxes):
    c.text(60, y - 26, title, fs=16, bold=True, anchor='start')
    geoms = []
    x = 90
    for lines in boxes:
        c.box_multi(x, y, 250, 86, lines)
        geoms.append((x, x + 250))
        x += 312
    for i in range(len(geoms) - 1):
        c.poly([(geoms[i][1], y + 43), (geoms[i + 1][0], y + 43)], marker='arr')


row(150, 'AS-IS — текущий процесс (курьер)', [
    ['Срочная заявка', 'лаборатории'],
    ['Вызов курьера', 'или транспорта'],
    ['Ожидание', 'курьера ~30 мин'],
    ['Доставка по городу', '40–90 мин, трафик'],
    ['Получение', 'в клинике'],
])
c.text(860, 285, 'Итого 70–120 мин · зависит от трафика · дорого · человеческий фактор',
       fs=13, italic=True)

row(470, 'TO-BE — аппаратно-программный комплекс на БПЛА', [
    ['Заявка на карте', '(администратор)'],
    ['Приём оператором', 'модель «такси»'],
    ['Полёт БПЛА', 'по прямой 8–15 мин'],
    ['Сброс груза', 'в точке (AUX)'],
    ['Журнал и', 'уведомление'],
])
c.text(860, 605, 'Итого 10–20 мин · не зависит от дорог · видео и телеметрия в реальном времени',
       fs=13, italic=True)

out = pathlib.Path(__file__).parent / 'as_is_to_be.svg'
out.write_text(c.svg('Рисунок 1 – Процесс срочной доставки: AS-IS и TO-BE'), encoding='utf-8')
print(out)
